function printc(msg,x,y,c)
	local w=print(msg,0,-8)
	print(msg,x-(w/2),y,c)
end


function printStatus()
	if t-tLastStatus<120 then
		printc(statusMsg,120,60,12)
	end
end


function setStatusMsg(msg)
	statusMsg=msg
	tLastStatus=t
end


function changeGps(mod)
	gps=gps+mod
	tDelay=60//gps
	setStatusMsg(gps.." generations/sec")
end


function changeCellColor(dir)
	if dir>0 then
		if cellColorI<12 then
			cellColorI=cellColorI+1
		else
			cellColorI=1
		end
	elseif dir<0 then
		if cellColorI>1 then
			cellColorI=cellColorI-1
		else
			cellColorI=12
		end
	end
end


function handlePause()
	running=not running
	if not running then
		setStatusMsg("Paused")
	end
end


function handleMouse()
	mX,mY,mClick=mouse()
	
	if not running and mClick then
		mX=mX//cellSize
		mY=mY//cellSize
		
		if mouseMode==0 then
			if grid[mX][mY] then
				mouseMode=1 --delete
			else
				mouseMode=2 --add
			end
		end
	
		if mX~=lastmX or mY~=lastmY then
			if mouseMode==1 then
				grid[mX][mY]=false
			elseif mouseMode==2 then
				grid[mX][mY]=true
			end
			lastmX,lastmY=mX,mY
		end
	elseif mouseMode>0 then
		mouseMode=0
		lastmX,lastmY=-1,-1
	end
end


function handleInput()
	if titleScr then
		if keyp(1) then
			aboutScr=not aboutScr
		elseif keyp(19) then
			titleScr=false
		end
	else
		if keyp(58) and gps<10 then
			changeGps(1)
		elseif keyp(59) and gps>1 then
			changeGps(-1)
		elseif keyp(60) then
			changeCellColor(1)
		elseif keyp(61) then
			changeCellColor(-1)
		elseif keyp(48) then
			handlePause()
		elseif keyp(18) then
			grid=randomGrid()
		elseif keyp(3) then
			grid=initGrid()
		else
			handleMouse()
		end
	end
end


grid=initGrid()

function TIC()
	if titleScr then
		drawTitleScr()
	else
		drawGrid(grid)
	end
	
	printStatus()
	
	handleInput()
	
	if t%tDelay==0 and running then
		grid=updateGrid(grid)
	end
 
	t=t+1
end

